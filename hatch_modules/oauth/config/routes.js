'use strict';

exports.routes = function (map) {
    map.get('/authorize', 'oauth#authorize');
    map.get('/exchange', 'oauth#exchange');
    map.post('/exchange', 'oauth#exchange');
    map.post('/grant', 'oauth#grant', {as: 'grant'});

    map.post('/widgets/auth/show', 'widgets/auth#show');
};